SELECT * FROM users; -- Este comando selecciona todas las columnas de la tabla "users" y muestra todos los registros.

SELECT name FROM users; -- Este comando selecciona solo la columna "name" de la tabla "users" y muestra los nombres de todos los usuarios en esa columna.

SELECT name, surname FROM users; -- Este comando selecciona las columnas "name" y "surname" de la tabla "users" y muestra los nombres y apellidos de todos los usuarios.